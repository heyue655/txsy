async function main() {
  console.log('🚀 开始初始化经典著作...\n');

  for (const item of classicBooks) {
    // 检查是否已存在
    const existing = await prisma.book.findFirst({
      where: { title: item.title, author: item.author },
    });
    if (existing) {
      console.log(`⏭️  跳过已存在: 《${item.title}》 - ${item.author}`);
      continue;
    }

    const book = await prisma.book.create({
      data: {
        title: item.title,
        author: item.author,
        era: item.era,
        soulColor: item.soulColor,
        isActive: true,
        sortOrder: 0,
      },
    });

    await prisma.authorPersona.create({
      data: {
        bookId: book.id,
        ...item.persona,
      },
    });

    console.log(`✅ 已创建: 《${item.title}》 - ${item.author} (ID: ${book.id})`);
  }

  console.log('\n🎉 经典著作初始化完成！');
}

main()
  .catch(e => { console.error('❌ 初始化失败:', e); process.exit(1); })
  .finally(() => prisma.$disconnect());
